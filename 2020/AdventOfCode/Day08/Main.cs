        public static void Run()
        {
            var (acc, loop, code) = ParseNoLoop();
            Console.WriteLine($"The ACC is at {acc} at the start of the loop");
            ResetData();
            code = code.Where(i => i.Op != "acc" && i.Arg != 0).ToList();
            
            Console.WriteLine($"");
            Console.WriteLine($"There are {code.Count} corrupt instruction candidates");
            foreach (var instructon in code)
            {
                FlipOp(instructon);
                (acc, loop, _) = ParseNoLoop();
                
                if (!loop)
                {
                    Console.WriteLine($"The ACC is at {acc} at the end of the program");
                    break;
                }
                FlipOp(instructon);
                ResetData();
            }
            Console.WriteLine($"Program repair completed");
        }

        private static (int Acc, bool Loop, List<Instruction> Code) ParseNoLoop()
        {
            var (ptr, acc) = (0, 0);
            var prog = new List<Instruction>();

            while (true)
            {
                if (ptr == data.Count)
                    return (acc, false, prog);
                if (data[ptr].Ran)
                    return (acc, true, prog);

                data[ptr].Ran = true;
                prog.Add(data[ptr]);
                (ptr, acc) = data[ptr].Execute(ptr, acc);
            }
        }
        private static void ResetData()
        {
            data.ForEach(i => i.Ran = false);
        }
        private static void FlipOp(Instruction instruction)
