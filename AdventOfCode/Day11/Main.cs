        private static char[,] RunSimulation(char[,] initGen, Func<char[,], char[,]> newGen)
        {
            while (true) {
                var nextGen = newGen(initGen);
                if (AreMatches(initGen, nextGen))
                    return nextGen;
                else
                    initGen = (char[,]) nextGen.Clone();
            }
        }
        
        private static char[,] NewGeneration_Part1(char[,] currentGen)
        {
            var newGen = (char[,]) currentGen.Clone();
            for(int y = 0; y<currentGen.GetLength(1); y++)
            {
                for (int x = 0; x < currentGen.GetLength(0); x++)
                {
                    if (currentGen[x, y] != '.')
                    {
                        var n = GetNeighbours_Part1(currentGen, x, y);

                        if (n == 0 && currentGen[x, y] == 'L')
                            newGen[x, y] = '#';

                        if (n >= 4 && currentGen[x, y] == '#')
                            newGen[x, y] = 'L';
                    }
                }
            }

            return newGen;
        }
        private static int GetNeighbours_Part1(char[,] gen, int x, int y)
        {
            var count = 0;
            for (int i = -1; i < 2; i++)
                for (int j = -1; j < 2; j++)
                    if ((i != 0 || j != 0) &&
                        x + i >= 0 && x + i < gen.GetLength(0) &&
                        y + j >= 0 && y + j < gen.GetLength(1) &&
                        gen[x + i, y + j] == '#')
                        count++;
            return count;
        }

        private static char[,] NewGeneration_Part2(char[,] currentGen)
        {
            var newGen = (char[,])currentGen.Clone();
            for (int y = 0; y < currentGen.GetLength(1); y++)
            {
                for (int x = 0; x < currentGen.GetLength(0); x++)
                {
                    if (currentGen[x, y] != '.')
                    {
                        var n = GetNeighbours_Part2(currentGen, x, y);

                        if (n == 0 && currentGen[x, y] == 'L')
                            newGen[x, y] = '#';

                        if (n >= 5 && currentGen[x, y] == '#')
                            newGen[x, y] = 'L';
                    }
                }
            }

            return newGen;
        }
        private static int GetNeighbours_Part2(char[,] gen, int x, int y)
        {
            var (w, h) = (gen.GetLength(0), gen.GetLength(1));

            return vectors.Select(v =>
            {
                var i = 1;
                while (true)
                {
                    var testX = x + v[0]*i;
                    var testY = y + v[1]*i;
                    if (testX < 0 || testX >= w || testY < 0 || testY >= h)
                        return '.';
                    if (gen[testX, testY] != '.')
                        return gen[testX, testY];
                    i++;
                }
            }).Count(x => x == '#');
        }
        private static List<int[]> vectors = new List<int[]>
        {
            new [] {-1, 0},
            new [] {-1, 1},
            new [] { 0, 1},
            new [] { 1, 1},
            new [] { 1, 0},
            new [] { 1,-1},
            new [] { 0,-1},
            new [] {-1,-1}
        };
        
        private static bool AreMatches(char[,] a, char[,] b)
        {
            for (int i = 0; i < a.GetLength(0); i++)
                for (int j = 0; j < a.GetLength(1); j++)
                    if (a[i, j] != b[i, j])
                        return false;
            return true;
        }
        private static int CountOccupied(char[,] gen)
        {
            var count = 0;
            foreach(var seat in gen)
                if (seat == '#')
                    count++;
            return count;
        }
        private static void PrettyPrint(char[,] gen)
